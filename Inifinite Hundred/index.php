                    <h3>Mobile Applications</h3>
                </div>

                <p>
                    Mobile apps are on the quickest growing media platforms. You can create a mobile app for just
                    about anything you can imagine and with the ability to write apps for systems like iOS, Android,
                    Windows phone, Symbian, Blackberry you can cover just about any mobile device your customers may
                    own.
                </p>
                <p>
                    There are three main platforms that run of 3 different programming languages:
                </p>

                <ul>
                    <li>iOS on Objective-C or Swift</li>
                    <li>Android on Java</li>
                    <li>Windows Phone on Net</li>
                </ul>
                <p>
                    these three platforms cover the vast majority of handheld and portable devices that are on the
                    market today.
                </p>

                <p>
                    On all three of these platforms, you can there are 3 types of apps:
                </p>
                <ul>
                    <li>Native Apps</li>
                    <li>Hybrid Apps</li>
                    <li>Web Apps</li>
                </ul>
                <p>
                    With these three app types, you can cover just about any app feature you can dream of and all three
                    of them vary so that you can fight find the right app, for the right budget and maintenance levels.
                </p>

                <div class="center">
                    <h3>Native Apps</h3>
                </div>

                <p>
                    Native Apps are developed exclusively for each operating system. App built for systems like iOS,
                    Android, Windows phone, Symbian, Blackberry require your programmer to be able to create multiple
                    versions of your app in various codes. They require the most amount of coding and therefore take the
                    longest to develop.
                </p>
                <p>
                    If a native app is done right, if will run the fastest and can run a more custom set of commands
                    as well as having access to more features on the device than the other types of apps.
                </p>

                <div class="center">
                    <h3>Hybrid Apps</h3>
                </div>
                <p>
                    Hybrid Apps offer a combination of 'online' and 'offline' features. they can send and retrieve data
                    from a webserver as well as store it locally for future use. these apps only require sporadic
                    internet connections and for the most part, can function without it. (some features may still need a
                    connection)
                </p>
                <p>
                    These apps are built using HTML5, CSS and JavaScript to display their information. They offer a
                    smaller range on features than a Native Application but can still use some function of a device like
                    the camera and geolocation (GPS location) etc.
                </p>
                <p>
                    These apps are faster and easier to develop as they offer a much smaller code requirement and almost
                    all platforms can display the same code base.
                </p>

                <div class="center">
                    <h3>Web Apps</h3>
                </div>
                <p>
                    Web Apps are simular to Hybrid applications in that they too that run the devices build in web
                    browser engine to display web-based content and these apps run code that are written in HTML5,
                    JavaScript and CSS. Where they differ is that all of the data in a Web Application is hosted on your
                    company's server and the app is effectively a closed off web browser
                </p>
                <p>
                    They are Perfect for a company that simple wishes to produce an app that displays their
                    website. They can display basic information and send and receive data from their users. The display
                    code will be on a web server and read to the device on request thus requiring and working internet
                    connection for the app to work.
                </p>
                <p>
                    These apps offer less device functions and can be relatively restrictive if you require more than
                    simple data sharing in your application.
                </p>

                <div class="center">
                    <h3>Server API</h3>
                </div>
                <p>
                    Whist technically a web technology, A server API is often required for a Mobile Application to
                    communicate with your system. Server API's allow your apps to retrieve and send data from your app
                    to your own database.
                </p>
                <p>
                    Most Server API's are written using markup languages such as Protocol Buffers, YAML, XML, JSON or
                    other formats.
                </p>
                <p>
                    At InfiniteHundred, we believe that each markup has its advantages. We like to use JSON for pure
                    data transfers and XML for transfers that include display properties.
                </p>


